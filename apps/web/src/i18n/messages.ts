const catalogs: Record<LanguageCode, MessageCatalog> = { en, de };

export function normalizeLanguage(value: string | null | undefined): LanguageCode | null {
    const normalized = (value ?? '').trim().toLowerCase();
    if (normalized.startsWith('de')) return 'de';
    if (normalized.startsWith('en')) return 'en';
    return null;
}

function interpolate(template: string, params: Params): string {
    return template.replace(/\{(\w+)\}/g, (_, key) => {
        const value = params[key];
        return value === null || value === undefined ? '' : String(value);
    });
}

export function translate(language: LanguageCode, key: string, params: Params = {}, fallback?: string): string {
    const value = catalogs[language][key] ?? catalogs.en[key];
    if (typeof value === 'function') {
        return value(params);
    }
    if (typeof value === 'string') {
        return interpolate(value, params);
    }
    return fallback ?? key;
}
